import java.util.Scanner;

public class CalculatorUtils {
    public static double averageOfTwoSalaries(double minSalary, double maxSalary){
        return ((minSalary + maxSalary) / 2.0);
    }
    public static double averageOfThreeSalaries(double minSalary, double maxSalary, double c){
        return ((minSalary + maxSalary + c) /3.0);
    }
}
